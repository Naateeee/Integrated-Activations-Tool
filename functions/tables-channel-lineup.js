    plan290CellTotal.textContent = totalChannelCounts["Plan 290"];
    plan520CellTotal.textContent = totalChannelCounts["Plan 520"];
    plan720CellTotal.textContent = totalChannelCounts["Plan 720"];
    plan1050CellTotal.textContent = totalChannelCounts["Plan 1050"];
    plan1350CellTotal.textContent = totalChannelCounts["Plan 1350"];
    plan1650CellTotal.textContent = totalChannelCounts["Plan 1650"];
    plan1990CellTotal.textContent = totalChannelCounts["Plan 1990"];

    // Update the thead cells with the HD channel counts
    allThingsEntCellHD.textContent = totalHDChannelCounts["Plan All Things Ent"];
    sportsActionCellHD.textContent = totalHDChannelCounts["Plan Sports Action"];
    pinoyAsianCellHD.textContent = totalHDChannelCounts["Plan Pinoy & Asian"];
    famTimeCellHD.textContent = totalHDChannelCounts["Plan Fam Time"];
    plan290CellHD.textContent = totalHDChannelCounts["Plan 290"];
    plan520CellHD.textContent = totalHDChannelCounts["Plan 520"];
    plan720CellHD.textContent = totalHDChannelCounts["Plan 720"];
    plan1050CellHD.textContent = totalHDChannelCounts["Plan 1050"];
    plan1350CellHD.textContent = totalHDChannelCounts["Plan 1350"];
    plan1650CellHD.textContent = totalHDChannelCounts["Plan 1650"];
    plan1990CellHD.textContent = totalHDChannelCounts["Plan 1990"];

    // Update the thead cells with the non-HD channel counts
    allThingsEntCellNonHD.textContent = totalNonHDChannelCounts["Plan All Things Ent"];
    sportsActionCellNonHD.textContent = totalNonHDChannelCounts["Plan Sports Action"];
    pinoyAsianCellNonHD.textContent = totalNonHDChannelCounts["Plan Pinoy & Asian"];
    famTimeCellNonHD.textContent = totalNonHDChannelCounts["Plan Fam Time"];
    plan290CellNonHD.textContent = totalNonHDChannelCounts["Plan 290"];
    plan520CellNonHD.textContent = totalNonHDChannelCounts["Plan 520"];
    plan720CellNonHD.textContent = totalNonHDChannelCounts["Plan 720"];
    plan1050CellNonHD.textContent = totalNonHDChannelCounts["Plan 1050"];
    plan1350CellNonHD.textContent = totalNonHDChannelCounts["Plan 1350"];
    plan1650CellNonHD.textContent = totalNonHDChannelCounts["Plan 1650"];
    plan1990CellNonHD.textContent = totalNonHDChannelCounts["Plan 1990"];
