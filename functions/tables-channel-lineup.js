// Function to replace "yes" with centered check symbol (✓)
function replaceYesWithCheck(channel) {
    for (let key in channel) {
        if (channel[key] === "yes") {
            channel[key] = "✓"; // Add spaces around the check symbol
        }
    }
    return channel;
}

